const SAT_T = new Array(SAT_TABLE.length);
const SAT_P = new Array(SAT_TABLE.length);
const SAT_HF = new Array(SAT_TABLE.length);
const SAT_HG = new Array(SAT_TABLE.length);

for (let i = 0; i < SAT_TABLE.length; i++) {
  const row = SAT_TABLE[i];
  SAT_T[i] = row[0];
  SAT_P[i] = row[1];
  SAT_HF[i] = row[2];
  SAT_HG[i] = row[3];
}

function clampNumber(x, min, max) {
  if (!Number.isFinite(x)) return min;
  return Math.max(min, Math.min(max, x));
}

function lerp(a, b, t) {
  return a + (b - a) * t;
}

function findInterval(xs, x) {
  // Returns i such that xs[i] <= x <= xs[i+1]. Assumes xs is sorted ascending.
  let lo = 0;
  let hi = xs.length - 1;

  if (x <= xs[0]) return 0;
  if (x >= xs[hi]) return hi - 1;

  while (hi - lo > 1) {
    const mid = (lo + hi) >> 1;
    if (xs[mid] <= x) lo = mid;
    else hi = mid;
  }

  return lo;
}

function interp1(xs, ys, x) {
  const xSafe = clampNumber(x, xs[0], xs[xs.length - 1]);
  const i = findInterval(xs, xSafe);
  const x0 = xs[i];
  const x1 = xs[i + 1];
  const y0 = ys[i];
  const y1 = ys[i + 1];
  const t = x1 === x0 ? 0 : (xSafe - x0) / (x1 - x0);
  return lerp(y0, y1, t);
}

export function satLiquidEnthalpyFromT(TC) {
  return interp1(SAT_T, SAT_HF, TC);
}

export function satTemperatureFromP(PMPa) {
  return interp1(SAT_P, SAT_T, PMPa);
}

export function satLiquidEnthalpyFromP(PMPa) {
  return interp1(SAT_P, SAT_HF, PMPa);
}

export function satVaporEnthalpyFromP(PMPa) {
  return interp1(SAT_P, SAT_HG, PMPa);
}

export function satTemperatureFromLiquidEnthalpy(hf) {
  // Invert h_f(T) using monotonic interpolation.
  return interp1(SAT_HF, SAT_T, hf);
}

export function computeThrottling({ T1C, P2MPa }) {
  const P1MPa = 20;
  const inletTempC = clampNumber(T1C, 100, 312);
  const outletPressureMPa = clampNumber(P2MPa, 0.1, 2);

  const h1 = satLiquidEnthalpyFromT(inletTempC);

  const hf2 = satLiquidEnthalpyFromP(outletPressureMPa);
  const hg2 = satVaporEnthalpyFromP(outletPressureMPa);

  let quality = (h1 - hf2) / (hg2 - hf2);
  if (!Number.isFinite(quality)) quality = 0;
  quality = clampNumber(quality, 0, 1);

  let phase2;
  if (quality <= 0) phase2 = 'liquid water';
  else if (quality >= 1) phase2 = 'vapor';
  else phase2 = 'vapor/liquid mixture';

  const isTwoPhase = quality > 0 && quality < 1;
  const T2C = isTwoPhase ? satTemperatureFromP(outletPressureMPa) : satTemperatureFromLiquidEnthalpy(h1);

  return {
    P1MPa,
    T1C: inletTempC,
    h1,
    P2MPa: outletPressureMPa,
    T2C,
    hf2,
    hg2,
    quality,
    phase1: 'liquid water',
    phase2
  };
}
